namespace WebExpress.WebApp.WebSection
{
    /// <summary>
    /// Definition of keys to identify sections in wen controls that can be occupied by components.
    /// </summary>
    public static class SectionControl
    {
        /// <summary>
        /// Returns the header.preferences section.
        /// </summary>
        public const string HeaderPreferences = "header.preferences";

        /// <summary>
        /// Returns the header.primary section.
        /// </summary>
        public const string HeaderPrimary = "header.primary";

        /// <summary>
        /// Returns the header.secondary section.
        /// </summary>
        public const string HeaderSecondary = "header.secondary";

        /// <summary>
        /// Returns the footer.preferences section.
        /// </summary>
        public const string FooterPreferences = "footer.preferences";

        /// <summary>
        /// Returns the footer.primary section.
        /// </summary>
        public const string FooterPrimary = "footer.primary";

        /// <summary>
        /// Returns the footer.secondary section.
        /// </summary>
        public const string FooterSecondary = "footer.secondary";
    }
}
