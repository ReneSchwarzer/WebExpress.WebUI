namespace WebExpress.WebUI.WebControl
{
    /// <summary>
    /// Specifies the layout options for items in a form.
    /// </summary>
    public enum TypeLayoutFormItem
    {
        /// <summary>
        /// Items are arranged vertically.
        /// </summary>
        Vertical,

        /// <summary>
        /// Items are arranged horizontally.
        /// </summary>
        Horizontal,

        /// <summary>
        /// Items are arranged in a mix of vertical and horizontal layouts.
        /// </summary>
        Mix
    }
}
